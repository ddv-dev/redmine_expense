module RedmineExpense
  # Остатки и количества показываются пользователю только целыми числами —
  # дробная часть (например, из-за объединения партий одного материала при
  # импорте) не нужна для отображения. На хранение и внутренние расчеты
  # (доступное количество, списание) это не влияет — округление только для
  # вывода на экран/в PDF/в Excel.
  module Quantity
    module_function

    def format(value)
      value.to_f.round
    end
  end
end
